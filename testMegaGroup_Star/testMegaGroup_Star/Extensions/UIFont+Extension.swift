import UIKit

extension UIFont {
	static func textFont() -> UIFont {
		return UIFont(name: "avenir", size: 16)!
	}
	static func titleFont() -> UIFont {
		return UIFont(name: "TrebuchetMS-Bold", size: 20)!
	}
}
