import UIKit

extension UIFont {

	static func font12() -> UIFont {
		return UIFont(name: "Helvetica", size: 12)!
	}
	static func font14() -> UIFont {
		return UIFont(name: "Helvetica", size: 14)!
	}
	static func font16() -> UIFont {
		return UIFont(name: "Helvetica", size: 16)!
	}
	static func font18() -> UIFont {
		return UIFont(name: "Helvetica", size: 18)!
	}
	static func font20() -> UIFont {
		return UIFont(name: "Helvetica", size: 20)!
	}
	static func font26() -> UIFont {
		return UIFont(name: "Helvetica", size: 26)!
	}
	static func titleFont() -> UIFont {
		return UIFont.systemFont(ofSize: 16, weight: .bold)
	}
	
}
